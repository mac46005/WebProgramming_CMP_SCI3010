"""
Operators are used to perform operations on variables and values

Python divides the operators in the following groups:

Arithmetic
Assignment
Comparison
Identity
Membership
"""

x = 4
y = 3

# Arithmetic Operators

print("Arithmetic Operators")
print(str(x) + "+" + str(y) + " = " + str(x + y))
print(str(x) + "-" + str(y) + " = " + str(x - y))
print(str(x) + "*" + str(y) + " = " + str(x * y))
print(str(x) + "/" + str(y) + " = " + str(x / y))
print(str(x) + "%" + str(y) + " = " + str(x % y))
print(str(x) + "**" + str(y) + " = " + str(x ** y))
print(str(x) + "//" + str(y) + " = " + str(x // y))


